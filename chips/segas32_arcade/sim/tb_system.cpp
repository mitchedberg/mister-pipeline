// =============================================================================
// 93C46 EEPROM model (64 × 16-bit = 128 bytes)
// Used by Rad Mobile (eeprom-radm.ic76) for high score / config storage.
// Interface via 315-5296 port D (C00006):
//   Bit 5 = CS  (chip select, active high)
//   Bit 6 = CLK (serial clock, rising-edge triggered)
//   Bit 7 = DI  (serial data in)
// Port F (C0000A) bit 7 = DO (serial data out, active high)
// Protocol: 93C46 16-bit mode (MAME: EEPROM_93C46_16BIT)
// =============================================================================
struct Eeprom93c46 {
    static constexpr int WORDS = 64;
    uint16_t mem[WORDS];

    // State machine
    enum class State { IDLE, START, ADDR, DATA_OUT, DATA_IN };
    State   state   = State::IDLE;
    int     bit_cnt = 0;        // bits remaining to shift
    uint8_t opcode  = 0;        // 2-bit opcode
    uint8_t addr    = 0;        // 6-bit address
    uint32_t shift  = 0;        // shift register
    uint16_t data_out = 0;      // word being shifted out
    int      data_out_bit = 0;  // bit index (MSB first, 15 down to 0)
    bool     write_enable = false;
    bool     do_bit = true;     // current DO output (default high = ready)

    // Previous CLK/CS for edge detection
    bool prev_cs  = false;
    bool prev_clk = false;

    bool load(const char* path) {
        FILE* f = fopen(path, "rb");
        if (!f) {
            fprintf(stderr, "[eeprom] WARNING: cannot open %s — using all-0xFF defaults\n", path);
            for (int i = 0; i < WORDS; i++) mem[i] = 0xFFFF;
            return false;
        }
        uint8_t buf[WORDS * 2];
        size_t n = fread(buf, 1, sizeof(buf), f);
        fclose(f);
        // 93C46 data is stored big-endian in the ROM file (MAME ROM_LOAD16_WORD big-endian)
        for (int i = 0; i < WORDS && (size_t)(i*2+1) < n; i++)
            mem[i] = ((uint16_t)buf[i*2] << 8) | buf[i*2+1];
        fprintf(stderr, "[eeprom] Loaded %s (%zu bytes, %d words)\n", path, n, WORDS);
        // Print first few words
        fprintf(stderr, "[eeprom] First 8 words: ");
        for (int i = 0; i < 8 && i < WORDS; i++)
            fprintf(stderr, "%04X ", mem[i]);
        fprintf(stderr, "\n");
        return true;
    }

    // Call on each posedge of clk_cpu with current CS/CLK/DI values from portd
    // Updates do_bit based on state machine
    void tick(bool cs, bool clk, bool di) {
        bool cs_rise  = (cs  && !prev_cs);
        bool cs_fall  = (!cs && prev_cs);
        bool clk_rise = (clk && !prev_clk && cs);

        // CS falling edge → reset to IDLE
        if (cs_fall || (!cs && state != State::IDLE)) {
            // Only reset if going to IDLE
            if (cs_fall) {
                state   = State::IDLE;
                bit_cnt = 0;
                do_bit  = true;  // DO high = ready/idle
            }
        }

        if (cs && clk_rise) {
            // Rising edge of CLK while CS is high
            switch (state) {
                case State::IDLE:
                    // Wait for start bit (DI=1)
                    if (di) {
                        state   = State::START;
                        bit_cnt = 0;
                        shift   = 0;
                    }
                    break;

                case State::START:
                    // Collect opcode bits (2 bits)
                    shift = (shift << 1) | (di ? 1 : 0);
                    bit_cnt++;
                    if (bit_cnt == 2) {
                        opcode  = (uint8_t)(shift & 0x3);
                        state   = State::ADDR;
                        shift   = 0;
                        bit_cnt = 0;
                    }
                    break;

                case State::ADDR:
                    // Collect 6-bit address
                    shift = (shift << 1) | (di ? 1 : 0);
                    bit_cnt++;
                    if (bit_cnt == 6) {
                        addr = (uint8_t)(shift & 0x3F);
                        // Decode opcode (93C46 opcode bits are MSB-first, 2 bits)
                        // Opcode bit sequence: first bit = MSB, second bit = LSB
                        // shift = (first_bit << 1) | second_bit
                        // READ  = 10b = 0x2
                        // WRITE = 01b = 0x1
                        // ERASE = 11b = 0x3
                        // Special (EWEN/EWDS/ERAL/WRAL) = 00b = 0x0
                        if (opcode == 0x2) {
                            // READ: output dummy 0 bit, then 16 data bits
                            uint8_t a = addr & (WORDS - 1);
                            data_out = mem[a];
                            data_out_bit = 16;  // 16 = dummy bit, then 15..0
                            state = State::DATA_OUT;
                            do_bit = false;     // dummy 0 bit first
                            bit_cnt = 0;
                            fprintf(stderr, "[eeprom] READ addr=%02X data=%04X\n", a, data_out);
                        } else if (opcode == 0x1) {
                            // WRITE: collect 16 data bits
                            shift = 0;
                            state = State::DATA_IN;
                            bit_cnt = 0;
                        } else if (opcode == 0x0) {
                            // Extended opcode: addr[5:4] encodes sub-op
                            // EWEN = 11xxxx (subop=0x3), EWDS = 00xxxx (subop=0x0)
                            // WRAL = 01xxxx (subop=0x1), ERAL = 10xxxx (subop=0x2)
                            uint8_t subop = (addr >> 4) & 0x3;
                            if (subop == 0x3) {
                                write_enable = true;   // EWEN
                                fprintf(stderr, "[eeprom] EWEN (write enabled)\n");
                            }
                            if (subop == 0x0) {
                                write_enable = false;  // EWDS
                                fprintf(stderr, "[eeprom] EWDS (write disabled)\n");
                            }
                            // ERAL (0x2) and WRAL (0x1) not implemented
                            state = State::IDLE;
                        } else {
                            // ERASE (opcode=0x3): set addr word = all 1s
                            if (write_enable) {
                                uint8_t a = addr & (WORDS - 1);
                                mem[a] = 0xFFFF;
                                fprintf(stderr, "[eeprom] ERASE addr=%02X\n", a);
                            }
                            state = State::IDLE;
                        }
                    }
                    break;

                case State::DATA_OUT:
                    // Shift out bits: data_out_bit starts at 16 (dummy), then 15..0
                    bit_cnt++;
                    data_out_bit--;
                    if (data_out_bit >= 0) {
                        do_bit = (data_out >> data_out_bit) & 1;
                    } else {
                        do_bit = true;
                        state = State::IDLE;
                    }
                    break;

                case State::DATA_IN:
                    // Shift in 16 data bits
                    shift = (shift << 1) | (di ? 1 : 0);
                    bit_cnt++;
                    if (bit_cnt == 16) {
                        if (write_enable) {
                            uint8_t a = addr & (WORDS - 1);
                            mem[a] = (uint16_t)(shift & 0xFFFF);
                            fprintf(stderr, "[eeprom] WRITE addr=%02X data=%04X\n", a, mem[a]);
                        }
                        do_bit = true;
                        state = State::IDLE;
                    }
                    break;
            }
        }

        prev_cs  = cs;
        prev_clk = clk;
    }
};

static Eeprom93c46 g_eeprom;
