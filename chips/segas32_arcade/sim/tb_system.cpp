// =============================================================================
// tb_system.cpp — Sega System 32 Verilator testbench
//
// Wraps segas32_top (V60 CPU + segas32_video) and drives:
//   - Two clocks: clk_cpu (16 MHz) and clk_pix (6.14 MHz), both from C++
//   - Reset
//   - Program ROM: loaded from binary file, served synchronously from C++ array
//   - GFX ROM: stubbed (returns 0xAAAAAAAA)
//
// IMPORTANT: The V60 core takes a plain 'clk' input (no enPhi1/enPhi2).
// We drive both clk_cpu and clk_pix directly from C++ — the RTL never
// generates its own clock enables.  This is the System 32 equivalent of
// the NMK arcade's enPhi1/enPhi2 pattern.
//
// Environment variables:
//   N_FRAMES   — number of vertical frames to simulate (default 10)
//   ROM_PROG   — path to program ROM binary (byte-addressed, little-endian)
//                Default: ../roms/radm_full_prog.bin (2MB, ROM_LOAD16_WORD_SWAP)
//   ROM_GFX    — path to GFX ROM binary (32-bit words, used by segas32_video)
//                If not set, uses stub (0xAAAAAAAA)
//   DUMP_VCD   — set to "1" to enable VCD trace (slow)
//   RAM_DUMP   — path for per-frame RAM dump binary
//               Format per frame: [4B LE frame#][64KB work RAM][32B misc]
//
// Output:
//   frame_NNNN.ppm — one PPM file per vertical frame
//
// Video resolution: 320×224 (System 32 standard)
//   Horizontal: 320 active + 208 blanking = 528 total pixels/line
//   Vertical:   224 active +  38 blanking = 262 total lines/frame
//   V60 runs at 16 MHz; pixel clock ~6.14 MHz (528×262×60 ≈ 8.29 MHz, use 16/3)
//   We run both clocks in a simple ratio from a single C++ loop.
//
// Clock scheme:
//   clk_cpu ticks every iteration (half-period = 1 unit)
//   clk_pix ticks every 3 iterations (approx 16/3 ratio of pixel to cpu clock)
//   One frame = 528 * 262 * (16/6.14) ≈ ~365,000 cpu clock edges
//
// Reference: segas32_top.sv, segas32_video.sv, MAME sega/segas32.cpp
// =============================================================================

