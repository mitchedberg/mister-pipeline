// =============================================================================
// Video timing constants (System 32 320×224)
// =============================================================================
static constexpr int VID_H_ACTIVE  = 320;
static constexpr int VID_V_ACTIVE  = 224;
static constexpr int VID_H_TOTAL   = 528;
static constexpr int VID_V_TOTAL   = 262;

// Clock ratio: clk_cpu runs N_CPU_PER_PIX times faster than clk_pix
// 16 MHz / (528*262*60) ≈ 2.6 — use integer ratio 3 (pixel clock ≈ 5.33 MHz)
static constexpr int N_CPU_PER_PIX = 3;

// =============================================================================
// Program ROM — 16 MB address space (V60 has 24-bit address bus)
// =============================================================================
static constexpr uint32_t ROM_SIZE = 16u * 1024u * 1024u;
static uint8_t g_rom[ROM_SIZE];

static bool load_rom(const char* path) {
    FILE* f = fopen(path, "rb");
    if (!f) {
        fprintf(stderr, "[tb] WARNING: cannot open ROM: %s\n", path);
        return false;
    }
    size_t n = fread(g_rom, 1, ROM_SIZE, f);
    fclose(f);
    fprintf(stderr, "[tb] Loaded ROM: %s (%zu bytes)\n", path, n);

    if (n > 0 && n <= 0x20000) {
        // Small boot ROM (e.g. epr-13693.ic21, 128KB): mirror 8× across 0x000000-0x0FFFFF
        for (uint32_t off = (uint32_t)n; off < 0x100000; off += (uint32_t)n) {
            uint32_t chunk = (uint32_t)n;
            if (off + chunk > 0x100000) chunk = 0x100000 - off;
            memcpy(g_rom + off, g_rom, chunk);
        }
        fprintf(stderr, "[tb] Mirrored boot ROM across 0x000000-0x0FFFFF\n");
    }
    // Mirror 0xF00000-0xFFFFFF (reset vector area) using the boot ROM portion.
    // For a 2MB full program ROM (radm_full_prog.bin), the first 128KB (0x00000-0x1FFFF)
    // is the boot ROM containing the reset vector at offset 0x1FFF0.
    // Mirror this 128KB block 8× across 0xF00000-0xFFFFFF so that
    // 0xFFFFF0 = 0xF00000 + 0xFFF0 + 7*0x20000 = maps to boot[0x1FFF0].
    {
        uint32_t boot_sz = (n > 0 && n <= 0x20000) ? (uint32_t)n : 0x20000u;
        for (uint32_t off = 0; off < 0x100000; off += boot_sz) {
            uint32_t chunk = boot_sz;
            if (off + chunk > 0x100000) chunk = 0x100000 - off;
            memcpy(g_rom + 0xF00000 + off, g_rom, chunk);
        }
    }
    fprintf(stderr, "[tb] Mirrored boot sector (128KB) x8 -> 0xF00000-0xFFFFFF\n");

    // Show reset vector (at 0xFFFFF0 for V60)
    fprintf(stderr, "[tb] Reset vector @ 0xFFFFF0: %02X %02X %02X %02X\n",
            g_rom[0xFFFFF0], g_rom[0xFFFFF1], g_rom[0xFFFFF2], g_rom[0xFFFFF3]);
    return true;
}

// Serve 16-bit little-endian word from g_rom at byte address
static inline uint16_t rom_read(uint32_t byte_addr) {
    byte_addr &= (ROM_SIZE - 1);
    return (uint16_t)g_rom[byte_addr] | ((uint16_t)g_rom[byte_addr + 1] << 8);
}

// =============================================================================
// GFX ROM — 4 MB address space (gfx_addr is 22-bit = 4MB max)
// Tile ROM (gfx1): 2 MB, 4×ROM_LOAD32_BYTE interleave (mpr-13519/21/20/22)
// Sprite ROM: 8 MB (ROM_LOAD64_BYTE, not accessed via this port in current RTL)
// =============================================================================
static constexpr uint32_t GFX_ROM_SIZE = 4u * 1024u * 1024u;
static uint8_t g_gfx[GFX_ROM_SIZE];

static bool load_gfx_rom(const char* path) {
    FILE* f = fopen(path, "rb");
    if (!f) {
        fprintf(stderr, "[tb] WARNING: cannot open GFX ROM: %s — using stub 0xAAAAAAAA\n", path);
        memset(g_gfx, 0xAA, sizeof(g_gfx));
        return false;
    }
    size_t n = fread(g_gfx, 1, GFX_ROM_SIZE, f);
    fclose(f);
    fprintf(stderr, "[tb] Loaded GFX ROM: %s (%zu bytes)\n", path, n);
    return true;
}

static inline uint32_t gfx_read(uint32_t addr) {
    addr &= (GFX_ROM_SIZE - 1);
    return ((uint32_t)g_gfx[addr])       |
           ((uint32_t)g_gfx[addr+1] << 8)  |
           ((uint32_t)g_gfx[addr+2] << 16) |
           ((uint32_t)g_gfx[addr+3] << 24);
}

// =============================================================================
// Frame buffer
// =============================================================================
struct FrameBuffer {
    static constexpr int W = VID_H_ACTIVE;
    static constexpr int H = VID_V_ACTIVE;
    std::vector<uint32_t> pixels;

    FrameBuffer() : pixels(W * H, 0) {}

    void set(int x, int y, uint8_t r, uint8_t g, uint8_t b) {
        if (x >= 0 && x < W && y >= 0 && y < H)
            pixels[y * W + x] = ((uint32_t)r << 16) | ((uint32_t)g << 8) | b;
    }

    bool write_ppm(const char* path) const {
        FILE* f = fopen(path, "wb");
        if (!f) { fprintf(stderr, "Cannot write %s\n", path); return false; }
        fprintf(f, "P6\n%d %d\n255\n", W, H);
        for (int y = 0; y < H; y++) {
            for (int x = 0; x < W; x++) {
                uint32_t p = pixels[y * W + x];
                uint8_t rgb[3] = { (uint8_t)(p >> 16), (uint8_t)(p >> 8), (uint8_t)p };
                fwrite(rgb, 1, 3, f);
            }
        }
        fclose(f);
        return true;
    }
};

// =============================================================================
// Write helpers for RAM dump
// =============================================================================
static inline void write_word_le(FILE* f, uint16_t w) {
    uint8_t b[2] = { (uint8_t)(w & 0xFF), (uint8_t)(w >> 8) };
    fwrite(b, 1, 2, f);
}
static inline void write_zeros(FILE* f, size_t n) {
    static const uint8_t zero_buf[4096] = {};
    while (n >= sizeof(zero_buf)) {
        fwrite(zero_buf, 1, sizeof(zero_buf), f);
        n -= sizeof(zero_buf);
    }
    if (n > 0) fwrite(zero_buf, 1, n, f);
}

// Dump per-frame WRAM to an individual file (matches MAME golden dump format).
// Format: raw 64KB, byte-addressed, little-endian words from work_ram[0..32767].
// File name: sim_wram_NNNNN.bin (one per frame).
static void dump_frame_wram_file(uint32_t frame_num, Vsegas32_top* top) {
    char fname[64];
    snprintf(fname, sizeof(fname), "sim_wram_%05u.bin", (unsigned)frame_num);
    FILE* f = fopen(fname, "wb");
    if (!f) {
        fprintf(stderr, "[tb] ERROR: cannot open %s for writing\n", fname);
        return;
    }
    // 32K words × 2 bytes = 64KB, little-endian word layout:
    //   word i low byte  → byte address 0x200000 + 2*i
    //   word i high byte → byte address 0x200001 + 2*i
    uint8_t buf[65536];
    for (int i = 0; i < 32768; i++) {
        uint16_t w = (uint16_t)top->rootp->segas32_top__DOT__work_ram[i];
        buf[i * 2]     = (uint8_t)(w & 0xFF);
        buf[i * 2 + 1] = (uint8_t)(w >> 8);
    }
    fwrite(buf, 1, 65536, f);
    fclose(f);
}

// Dump per-frame RAM state to combined file.
// Format: [4B LE frame#][64KB work_ram little-endian words][32B zeros pad]
static void dump_frame_ram(FILE* f, uint32_t frame_num, Vsegas32_top* top) {
    // 4-byte LE frame number
    uint8_t hdr[4] = {
        (uint8_t)(frame_num & 0xFF),
        (uint8_t)((frame_num >> 8) & 0xFF),
        (uint8_t)((frame_num >> 16) & 0xFF),
        (uint8_t)((frame_num >> 24) & 0xFF)
    };
    fwrite(hdr, 1, 4, f);

    // Work RAM: 32K words (64KB), little-endian word layout
    uint8_t buf[65536];
    for (int i = 0; i < 32768; i++) {
        uint16_t w = (uint16_t)top->rootp->segas32_top__DOT__work_ram[i];
        buf[i * 2]     = (uint8_t)(w & 0xFF);
        buf[i * 2 + 1] = (uint8_t)(w >> 8);
    }
    fwrite(buf, 1, 65536, f);
    // Pad to round number
    write_zeros(f, 32);
}

